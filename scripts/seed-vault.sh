#!/usr/bin/env bash
# seed-vault.sh — Generate a comprehensive throwaway vault that exercises every
# rendering form, page type, wikilink/backlink, transclusion, and exclusion the
# native renderer supports. Use it for visual verification (Playwright) and for
# manual spot-checks of CSS/template/renderer changes.
#
# Idempotent: the target directory is wiped and recreated on every run, so the
# output is deterministic. It only ever touches the target dir (default
# /tmp/wiki-test-vault) — zero side effects elsewhere.
#
# Coverage map (one feature per page where practical):
#   index.md                     root folder-index → "/"  + intro callout
#   getting-started.md           core GFM: headings, emphasis, lists, task
#                                lists, tables, code, blockquote, hr, footnotes,
#                                definition lists, emoji, links, images
#   rendering/index.md           nested folder-index → "/rendering/"
#   rendering/callouts.md        all 13 callout kinds + foldable (+/-)
#   rendering/code-blocks.md     syntax highlighting across several languages
#   rendering/math.md            inline $x$ + block $$…$$ (KaTeX)
#   rendering/diagrams.md        ```mermaid flowchart + sequence
#   rendering/obsidian.md        ==highlight==, %%comment%%, ^block-id, #tags
#   rendering/embeds.md          markdown images, YouTube/Vimeo provider embeds,
#                                native file embeds (audio/pdf/video)
#   wikilinks/source.md          [[target]], [[target|alias]], [[target#h]],
#                                [[target#^block]] — drives backlinks on target
#   wikilinks/target.md          headings + ^block-id; links back to source
#   transclusion/a→b→c→d.md      ![[…]] chain to exercise the depth-3 limit
#   transclusion/partial.md      ![[target#heading]] + ![[target#^block]]
#   raw/*.{txt,yaml,sh}          native source files + /raw directory listing
#
# Shared tags (#demo #rendering #reference) span multiple pages so tag-listing
# pages have more than one entry.

set -euo pipefail

# --- CLI -------------------------------------------------------------------
# Usage: scripts/seed-vault.sh [TARGET_DIR]   (default: /tmp/wiki-test-vault)
VAULT="${1:-/tmp/wiki-test-vault}"

if [[ "$VAULT" != /* && "$VAULT" != ./* ]]; then
	VAULT="./$VAULT" # guard against an accidental `rm -rf` on a bare name
fi

# --- helpers ---------------------------------------------------------------
# page <relative/path.md> reads file body from stdin and writes it under $VAULT,
# creating parent directories as needed.
page() {
	local dest="$VAULT/$1"
	mkdir -p "$(dirname "$dest")"
	cat >"$dest"
}

# b64file <relative/path> base64-decodes stdin into a binary file under $VAULT.
# Used for the small valid media fixtures (wav) that drive native file embeds.
b64file() {
	local dest="$VAULT/$1"
	mkdir -p "$(dirname "$dest")"
	base64 -d >"$dest"
}

# --- (re)create the vault --------------------------------------------------
rm -rf "$VAULT"
mkdir -p "$VAULT"

# ---------------------------------------------------------------------------
# Root index — folder index served at "/"
# ---------------------------------------------------------------------------
page index.md <<'EOF'
---
title: Test Vault
description: A throwaway vault that exercises every renderer feature.
tags: [demo]
date: 2026-01-01
maintainer: seed-vault.sh
status: fixture
homepage: https://github.com/jedwards1230/my-wiki
---

# Test Vault

This vault is generated by `scripts/seed-vault.sh` to exercise the native
renderer end to end. Start here, then walk the sections.

> [!info] Visual verification fixture
> Every page targets a specific rendering feature so screenshots cover the
> whole surface in light and dark mode.

## Sections

- [[getting-started]] — core Markdown / GFM
- [[rendering/index|Rendering reference]] — callouts, code, math, diagrams
- [[wikilinks/source]] and [[wikilinks/target]] — links + backlinks
- [[transclusion/a]] — embed/transclusion chain
- See `/raw/` for the native source-file listing

The frontmatter above also drives the **properties table** (maintainer,
status, homepage) — the last is auto-linked because it is a URL.
EOF

